USE Atif

SELECT 
	WORKER_ID
	FIRST_NAME,
	LAST_NAME,
	SALARY,
	JOINING_DATE,
	DEPT_NAME		
FROM WORKER;

--1
SELECT
	MAX(SALARY) AS MaxSalary,
	MIN(SALARY) AS MinSalary
FROM WORKER

--2
SELECT FIRST_NAME,
	REVERSE(FIRST_NAME) AS FIRST_NAME_REVERSED
FROM WORKER;

--3
SELECT FIRST_NAME,
	YEAR(JOINING_DATE) AS JOINING_YEAR
FROM WORKER

--4
SELECT
	DATEDIFF(DAY, JOINING_DATE, GETDATE()) AS TOTAL_DAY
FROM WORKER

