	WORKER_ID, 
	FIRST_NAME,
	LAST_NAME,
	CONVERT(INT, SALARY) AS SALARY,
	CONVERT(VARCHAR(15), JOINING_DATE, 101) AS JOINING_DATE, DEPT_NAME
FROM WORKER
